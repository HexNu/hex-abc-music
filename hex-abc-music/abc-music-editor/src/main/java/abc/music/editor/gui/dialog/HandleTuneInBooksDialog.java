    private void updateBookList() {
        booksPanel.removeAll();
        editor.getProject().getBooks().stream().forEach(this::addToBookList);
        booksPanel.repaint();
        booksPanel.revalidate();
    }

    private void addToBookList(Book book) {
        booksPanel.add(new BookItem(book));
    }

